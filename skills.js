function multiplicateNumbers(a, b) {
  return a * b;
}

function sumNumbers(a, b) {
  return a + b;
}

function divideNumbers(a, b) {
    return a / b;
}

function substractNumbers(a, b) {
    return a - b;
}

module.exports = {
    multiplicateNumbers,
    sumNumbers,
    divideNumbers,
    substractNumbers
}

